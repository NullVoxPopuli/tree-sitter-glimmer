static inline bool sym_attribute_name_character_set_1(int32_t c) {
  return (c < '\''
    ? (c < '\r'
      ? (c < '\t'
        ? c == 0
        : c <= '\n')
      : (c <= '\r' || (c >= ' ' && c <= '"')))
    : (c <= ')' || (c < '<'
      ? (c < '.'
        ? c == ','
        : c <= '/')
      : (c <= '?' || (c >= '{' && c <= '}')))));
}

